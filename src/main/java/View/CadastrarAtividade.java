    private void jbCadastrarAtividadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarAtividadeActionPerformed
        String titulo = jtAtividadeTitulo.getText();
        String data = jtAtividadeData.getText();
        String descricao = jtAtividaDescricao.getText();
        int x = cbMateria.getSelectedIndex();
        //String petName = (String)a.getSelectedItem();
        Materia item = (Materia)cbMateria.getSelectedItem();
        
        if (titulo.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Titulo!");
            return;
        }
        
        if (data.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Data!");
            return;
        }
        
        if (descricao.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Descrição!");
            return;
        }
        
        
        Atividade p = new Atividade();
        p.setTitulo(titulo);
        p.setDate(data);
        p.setDescricao(descricao);
        p.setMateria_id("" + item.getId());
        try {
            if (atualizando) {
                p.atualizarAtividade();
            }
            else {
                p.adicionarAtividade();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            return;
        }
        atualizando = true;
        carregaAtividade();
        
        JOptionPane.showMessageDialog(this, "Atividade salvo com sucesso!");
    }//GEN-LAST:event_jbCadastrarAtividadeActionPerformed

