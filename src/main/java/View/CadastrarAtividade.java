    public void carregaAtividade() {
        try {
            atv = Atividade.buscarAtividade();
            preencheCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar Produtos!");
            this.dispose();
        }    
    }
    
    public void preencheCampos() {
        
        edtCodigo.setEnabled(false);
        
        if (atv.size() > 0) {
            edtCodigo.setText(String.valueOf(atv.get(prodAtual).getId()));
            jtAtividadeTitulo.setText(atv.get(prodAtual).getTitulo());
            jtAtividadeData.setText(String.valueOf(atv.get(prodAtual).getDate()));
            jtAtividaDescricao.setText(atv.get(prodAtual).getDescricao());
            
            if (prodAtual == 0) {
                jbAnterio.setEnabled(false);
            }
            else {
               jbAnterio.setEnabled(true);
            }

            if (prodAtual == atv.size() - 1) {
                jbProximo.setEnabled(false);
            }
            else {
                jbProximo.setEnabled(true);
            }
        }
        else {
            edtCodigo.setText("Automático...");
            jbAnterio.setEnabled(false);
            jbProximo.setEnabled(false);
            jbCadastrarAtividade.setEnabled(true);
            atualizando = false;
        }
    }
