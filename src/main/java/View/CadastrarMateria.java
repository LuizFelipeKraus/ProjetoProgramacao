     public void carregarMateria() {
        try {
            mat = Materia.buscarMateria();
            preencheCampos();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar Produtos!");
            this.dispose();
        }    
    }
    public void preencheCampos() {
        
        jtCodigo.setEnabled(false);
        
        if (mat.size() > 0) {
            jtCodigo.setText(String.valueOf(mat.get(prodAtual).getId()));
            jtMateriaNome.setText(mat.get(prodAtual).getNome());
            jtMateriaData.setText(String.valueOf(mat.get(prodAtual).getHorario()));
            jtMateriaDuracao.setText(mat.get(prodAtual).getDuracao() + "H");
            
            if (prodAtual == 0) {
                jbAnterio.setEnabled(false);
            }
            else {
                jbAnterio.setEnabled(true);
            }

            if (prodAtual == mat.size() - 1) {
                jbProximo.setEnabled(false);
            }
            else {
                jbProximo.setEnabled(true);
            }
        }
        else {
            jtCodigo.setText("Automático...");
            jbAnterio.setEnabled(false);
            jbProximo.setEnabled(false);
            jbCadastrarMateria.setEnabled(true);
            atualizando = false;
        }
    }
