    private void jbCadastrarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarMateriaActionPerformed
        String nome = jtMateriaNome.getText();
        String data = jtMateriaData.getText();
        String duracao = jtMateriaDuracao.getText();
        
        if (nome.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Nome!");
            return;
        }
        
        if (data.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Data!");
            return;
        }
        
        if (duracao.length() == 0) {
            JOptionPane.showMessageDialog(this, "Você deve informar a Duração!");
            return;
        }
        
        
        Materia p = new Materia();
        p.setNome(nome);
        p.setHorario(data);
        p.setDuracao(duracao);
                
        try {
            if (atualizando) {
                p.atualizarMateria();
            }
            else {
                p.adicionarMateria();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            return;
        }
        atualizando = true;
        carregarMateria();
        
        JOptionPane.showMessageDialog(this, "Materia salva com sucesso!");
    }//GEN-LAST:event_jbCadastrarMateriaActionPerformed

